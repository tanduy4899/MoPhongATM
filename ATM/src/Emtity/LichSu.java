package Emtity;

import java.util.Date;

public class LichSu {
	int ID;
	String SoTaiKhoan;
	float TienRut;
	float TienChuyen;
	Date ThoiGian;
	
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getSoTaiKhoan() {
		return SoTaiKhoan;
	}
	public void setSoTaiKhoan(String soTaiKhoan) {
		SoTaiKhoan = soTaiKhoan;
	}
	public float getTienRut() {
		return TienRut;
	}
	public void setTienRut(float tienRut) {
		TienRut = tienRut;
	}
	public float getTienChuyen() {
		return TienChuyen;
	}
	public void setTienChuyen(float tienChuyen) {
		TienChuyen = tienChuyen;
	}
	public Date getThoiGian() {
		return ThoiGian;
	}
	public void setThoiGian(Date thoiGian) {
		ThoiGian = thoiGian;
	}
	

}
